     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'nama_pembayaran' => 'required',

        ]);

        $pembayaran = Pembayaran::create([
            'nama_pembayaran' => $request->input('nama_pembayaran'),
        ]);

        if ($pembayaran) {
            return redirect()->route('pembayaran.index')->with('success', 'Data pembayaran berhasil ditambahkan');
        } else {
            return redirect()->route('pembayaran.index')->with('error', 'Data pembayaran gagal ditambahkan');
        }    }

